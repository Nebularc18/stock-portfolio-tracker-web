export function t(language: Language, key: string, params?: Record<string, string | number>): string {
  const template = translations[language][key] ?? translations.en[key] ?? key
  if (!params) return template

  return Object.entries(params).reduce((acc, [paramKey, paramValue]) => {
    return acc.split(`{{${paramKey}}}`).join(String(paramValue))
  }, template)
}

export function getLocaleForLanguage(language: Language): string {
  return language === 'sv' ? 'sv-SE' : 'en-US'
}
